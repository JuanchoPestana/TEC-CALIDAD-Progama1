// Clase de Archivo. Aqui se crea un archivo con lineas blancas, nombre y lineas de informacion.
//=b-0
#ifndef Archivo_h
#define Archivo_h

using namespace std;


class Archivo{
    
public:
    
    // CONSTRUCTORES
    Archivo();
    Archivo(string Nombre, int LineasBlancas, int LineasInformacion);
    
    // SETS
    void setNombre(string Nombre);
    void setLineasBlancas(int LineasBlancas);
    void setLineasInformacion(int LineasInformacion);
    
    
    // GETS
    string getNombre();
    int getLineasBlancas();
    int getLineasInformacion();
    
private:
    
    // VARIABLES
    string Nombre;
    int LineasBlancas;
    int LineasInformacion;
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
// CONSTRUCTOR DEFAULT
Archivo :: Archivo(){
    
    Nombre = "";
    LineasBlancas = 0;
    LineasInformacion = 0;
    
}// END CONSTRUCTOR DEFAULT

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
// CONSTRUCTOR CON PARAMETROS
Archivo :: Archivo(string Nombre, int LineasBlancas, int LineasInformacion){
    
    this->Nombre = Nombre;
    this->LineasBlancas = LineasBlancas;
    this->LineasInformacion = LineasInformacion;
    
}// END CONSTRUCTOR CON PARAMETROS

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
// SETS

void Archivo :: setNombre(string Nombre){
    
    this->Nombre = Nombre;
    
}// END NOMBRE


void Archivo :: setLineasBlancas(int LineasBlancas){
    
    this->LineasBlancas = LineasBlancas;
    
}// END LINEAS BLANCAS


void Archivo :: setLineasInformacion(int LineasInformacion){
    
    this->LineasInformacion = LineasInformacion;
    
}// END LINEAS INFORMACION

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
// GETS

string Archivo :: getNombre(){
    
    return Nombre;
    
}// END NOMBRE


int Archivo :: getLineasBlancas(){
    
    return LineasBlancas;
    
}// END LINEAS BLANCAS


int Archivo :: getLineasInformacion(){
    
    return LineasInformacion;
    
}// END LINEAS INFORMACION

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
// END




#endif /* Archivo_h */
